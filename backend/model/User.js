const mongoose = require("mongoose");

// now we will create schema that represents model of our user
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    min: 6,
  },
  email: {
    type: String,
    required: true,
    max: 255,
    min: 6,
  },
  password: {
    type: String,
    required: true,
    max: 1024,
    min: 6,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

//export it to, where when we have creater
//router of register
module.exports = mongoose.model("User", userSchema);
